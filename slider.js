let currentImageIndex = 0;

const sliderContainer = document.querySelector(".slider-container");
sliderContainer.style.backgroundImage = `url(${bgImageUrls[currentImageIndex]})`;

// previous button
const previousE1 = document.querySelector(".previous");

previousE1.addEventListener("click", ()=> {
  previousE1.style.transform = "translateX(-20px)";
  setTimeout(() => {
    previousE1.style.transform = "translateX(0)";
  }, 300);

  currentImageIndex--;
  if (currentImageIndex < 0) {
    currentImageIndex = bgImageUrls.length;
  }
  sliderContainer.style.backgroundImage = `url(${bgImageUrls[currentImageIndex]})`;
});


//next button

const nextE1 = document.querySelector(".next");

nextE1.addEventListener("click", ()=> {
  nextE1.style.transform = "translateX(20px)";
  setTimeout(() => {
    nextE1.style.transform = "translateX(0)";
  }, 300);

  currentImageIndex++;
  if (currentImageIndex >= bgImageUrls.length) {
    currentImageIndex = 0;
  }
  sliderContainer.style.backgroundImage = `url(${bgImageUrls[currentImageIndex]})`;
});


